    public void showTableData(String dbTableName, JTable uiTable){
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/airlines", "root", "");
            sql = "SELECT * FROM "+dbTableName;
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            uiTable.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    public void Delete(String dbTableID, JTextField tf){
        try{
            sql = "DELETE FROM "+dbTableID+" WHERE ID = ?";
            dbConnection = JConnection.ConnecrDB();
            pst = dbConnection.prepareStatement(sql);
            pst.setString(1, tf.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data deleted successfully");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
