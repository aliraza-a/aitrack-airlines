            dbTableName = "pilot";
            sql = "INSERT INTO `pilot`"
                +"(`Name`, `Contact`, `Address`)"
                +"VALUES (?,?,?)";
            dbConnection = JConnection.ConnecrDB();
            pst = dbConnection.prepareStatement(sql);
            pst.setString(1,txtPName.getText());
            pst.setString(2,txtPCon.getText());
